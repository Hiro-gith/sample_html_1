// 足し算
console.log(10+3);

// 引き算
console.log(10-3);

// 掛け算
console.log(10*3);

// 割り算
console.log(10/3);

// 余り
console.log(10%3);

// 変数
var a =100;
var str = "javascript";

console.log(a);
console.log(str);

//後置演算
var x = 10;
var y = x++;
// x=10
console.log(x);
// y=11
console.log(y);

//前置演算
var i = 10;
var j = ++i;
// i=11
console.log(i);
// j=11
console.log(j);

// 条件分岐
var num=100;
if(num >=100){
  console.log('100以上の値')
}else{
  console.log('100未満の値')
}